# Function to counts the number of live neighbors for a given cell in the grid
def count_live_neighbors(grid, row, col):
    rows = len(grid)
    cols = len(grid[0])
    count = 0
    for i in range(-1, 2):
        for j in range(-1, 2):
            if not (i == 0 and j == 0):
                neighbor_row = (row + i + rows) % rows
                neighbor_col = (col + j + cols) % cols
                count += grid[neighbor_row][neighbor_col]
    return count

# Function to calculate the next generation of the grid based on the rules of Conway's Game of Life
def next_generation(grid):
    rows = len(grid)
    cols = len(grid[0])
    new_grid = [[0] * cols for _ in range(rows)]
    for i in range(rows):
        for j in range(cols):
            live_neighbors = count_live_neighbors(grid, i, j)
            if grid[i][j] == 1 and (live_neighbors < 2 or live_neighbors > 3):
                new_grid[i][j] = 0
            elif grid[i][j] == 0 and live_neighbors == 3:
                new_grid[i][j] = 1
            else:
                new_grid[i][j] = grid[i][j]
    return new_grid

# Function to toggle the state of a cell at the specified row and column in the grid
def toggle_cell_state(grid, row, col):
    grid[row][col] = 1 if grid[row][col] == 0 else 0

# Function to simulate one generation of the game
def simulate_generation(grid):
    return next_generation(grid)

# Function to display the grid with live cells and generation number
def display_grid(grid, generation):
    os.system("cls")
    print("Conway's Game of Life!")
    for i, row in enumerate(grid):
        for j, cell in enumerate(row):
            if cell == 1:
                print("0", end=" ")
            else:
                print("-", end=" ")
        print()
    print("Generation:", generation)

# Main function to run the game
def main():
    speed = 1
    generation = 0
    pattern = "Conway's Pulsar"
    grid = initialize_grid(pattern)
    initial_grid = copy.deepcopy(grid)  # Save the initial state of the grid
    while True:

        # Main menu
        menu_selection = print_menu([
            "Welcome to Conway's Game of Life!",
            "1. Select initial pattern",
            "2. Control simulation speed",
            "3. Start Game",
            "4. Quit Game"
        ])

        if menu_selection == "1":
            # Pattern selection menu
            pattern_selection = print_menu([
                "Select Initial Pattern",
                "1. Gosper's Glider Gun",
                "2. Berger's Skewed Quad",
                "3. Conway's Pulsar",
                "4. Back to Main Menu"
            ])

            if pattern_selection == "1":
                pattern = "Gosper's Glider Gun"
                grid = initialize_grid(pattern)
            elif pattern_selection == "2":
                pattern = "Berger's Skewed Quad"
                grid = initialize_grid(pattern)
            elif pattern_selection == "3":
                pattern = "Conway's Pulsar"
                grid = initialize_grid(pattern)
            elif pattern_selection == "4":
                continue

        elif menu_selection == "2":
            # Control simulation speed menu
            speed_selection = print_menu([
                "Control Simulation Speed",
                "1. One generation at a time",
                "2. Three generations at a time",
                "3. Ten generations at a time",
                "4. Back to Main Menu"
            ])

            if speed_selection == "1":
                speed = 1
            elif speed_selection == "2":
                speed = 3
            elif speed_selection == "3":
                speed = 10
            elif speed_selection == "4":
                continue

        elif menu_selection == "3":
            # Start game
            while True:
                display_grid(grid, generation)
                action = print_menu([
                    "1. Toggle cell state (to change)",
                    "2. Continue the next generation(s)",
                    "3. Quit Game"
                ])

                if action == "1":
                    # Toggle cell state to change
                    indices = input("Enter the row, column indices separated by a comma: ").split(",")
                    row, col = int(indices[0]), int(indices[1])
                    toggle_cell_state(grid, row, col)
                elif action == "2":
                    # Continue the next generation
                    for _ in range(speed):
                        grid = simulate_generation(grid)
                        generation += 1
                elif action == "3":
                    # Quit game
                    grid = reset_grid(initial_grid)
                    generation = 0
                    speed = 1
                    clear_screen()
                    break

        elif menu_selection == "4":
            # Quit game
            break

if __name__ == "__main__":
    main()
